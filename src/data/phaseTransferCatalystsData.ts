export const getProductColor = (productName: string): string => {
  const colorMap: { [key: string]: string } = {
    'tetra': 'orange',
    'benzyl': 'purple',
    'ethyl': 'blue',
    'cetyl': 'green',
    'tri': 'teal',
    'ammonium': 'orange',
    'phosphonium': 'indigo'
  };
  
  const productLower = productName.toLowerCase();
  
  for (const [keyword, tailwindColor] of Object.entries(colorMap)) {
    if (productLower.includes(keyword)) {
      return tailwindColor;
    }
  }
  
  return 'orange'; // Default to orange for catalysts
};

export const getProductId = (productName: string): string => {
  return productName.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-+|-+$/g, '');
};

